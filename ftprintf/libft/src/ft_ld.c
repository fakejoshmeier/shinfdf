#include "libft.h"

void	ft_ld(long n)
{
	if (n < 0)
		n *= -1;
	if (n == -4294967295)
	{
		n *= -1;
		ft_putchar('4');
		n %= 1000000000;
	}
	if (n >= 10)
	{
		ft_ld(n / 10);
		ft_ld(n % 10);
	}
	else
		ft_putchar(n + '0');
}
