#include "libft.h"

void	ft_strclr(char *s)
{
	char	*str;
	int		i;
	int		j;

	if (!s)
		return ;
	i = (int)ft_strlen(s);
	j = -1;
	str = s;
	while (j < i + 1)
		str[++j] = 0;
}
