#include "libft.h"

void	ft_putnbrf(int n)
{
	if (n < 0)
		n = -n;
	if (n == -2147483648)
	{
		ft_putchar('2');
		n = n % 1000000000;
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbrf(n / 10);
		ft_putnbrf(n % 10);
	}
	else
		ft_putchar(n + '0');
}
